package com.example.menuaplication.model.actividades;

/**
 * Enumerado que define los niveles de urgencia o importancia de una actividad.
 *
 * @author José Paladines
 * @version 1.0
 */
public enum Prioridad {
    /** Máxima urgencia. Se suele resaltar visualmente en rojo. */
    ALTA,

    /** Urgencia normal. Se suele resaltar en naranja. */
    MEDIA,

    /** Baja urgencia. Se suele resaltar en verde. */
    BAJA
}