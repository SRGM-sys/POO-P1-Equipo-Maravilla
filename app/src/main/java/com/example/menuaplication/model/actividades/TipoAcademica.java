package com.example.menuaplication.model.actividades;

/**
 * Enumerado que categoriza el tipo de entrega o evaluación de una actividad académica.
 *
 * @author José Paladines
 * @version 1.0
 */
public enum TipoAcademica {
    /** Evaluación formal o test. */
    EXAMEN,

    /** Deberes o trabajos regulares. */
    TAREA,

    /** Trabajo de investigación o entrega final extensa. */
    PROYECTO
}